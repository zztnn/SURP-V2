// Validación estática del catálogo (se ejecuta al importar el módulo).
// Detecta duplicados antes de que arranque la app.
(() => {
  const seen = new Set<string>();
  for (const p of PERMISSIONS_CATALOG) {
    if (seen.has(p.code)) {
      throw new Error(`PERMISSIONS_CATALOG: code duplicado '${p.code}'`);
    }
    seen.add(p.code);
    const expected = `${p.module}.${p.resource}.${p.action}`;
    if (p.code !== expected) {
      throw new Error(
        `PERMISSIONS_CATALOG: code '${p.code}' no coincide con module.resource.action='${expected}'`,
      );
    }
  }
})();
