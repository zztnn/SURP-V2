// Catálogo fijo de permisos del sistema. Fuente de verdad: este archivo.
// Los seeds SQL son una pre-carga; el `PermissionsSyncService` ejecuta
// UPSERT al arranque para mantener BD ↔ código sincronizados.
//
// Convención del code: `modulo.recurso.accion`. is_sensitive=true marca
// permisos cuya invocación se audita como lectura sensible (ADR-B-009).
//
// Reglas:
//   1. Agregar permisos solo aquí (no en seeds nuevos). El seed inicial
//      `database/seed/02_permissions.sql` puede quedar desactualizado;
//      este catálogo lo reconcilia al arranque.
//   2. NUNCA renombrar un `code` — los `role_permissions` lo referencian.
//      Si hay que cambiar, agregar el nuevo + mantener el viejo + migrar.
//   3. NUNCA borrar un `code` sin verificar que ningún `role_permissions`
//      lo use (el FK no permite borrar pero el catálogo se marcaría
//      inconsistente).

